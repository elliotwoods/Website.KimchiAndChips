    <div class="works_02">
        <div class="text_kr_works_02">
            <span class="text_en_bold">Mimi Son</span> (KR)
            손미미. 1971년 서울에서 태어나고 자랐다. 
            <p></p>작품을 만드는 과정과 완성을 통해서 존재, 실체, 관계에 대한 불교철학과 우연히 마주쳤고 이는 작품에 중요한 개념으로 자리 잡았다. 
            상이한듯 하지만 무척이나 닮은 예술과 기술의 수련 과정과 자연과 과학의 탐구 과정을 좋아한다.
            <p></p>
            디자이너로, 교수로, 예술감독으로 일해왔고 2009년부터 엘리엇 우즈와 함께 김치앤칩스에서 작품을 만들어 오고 있다. 
            런던의 미들섹스대학에서 디지털미디어아트를 코펜하겐의 CIID에서 인터랙션디자인을 수학하였다.   
        </div>   
        <div class="padding-80"></div>
        <div class="text_en">
            <span class="text_en_bold">Elliot Woods</span> (UK) is a digital media artist from Manchester. He tests 
            possible futures between humans and visual design technologies (e.g. 
            cameras, projectors, computation). Towards this goal, Elliot co-founded 
            Kimchi and Chips, an experimental art studio based in Seoul with Mimi Son. 
            He applies his academic studies in physics to produce sense-able 
            phenomena from abstract domains.
            <p></p>
            Elliot is a contributor to the openFrameworks project (a ubiquitous toolkit 
            for creative coding), and an open source contributor to the VVVV platform. 
            His code is available open source and for free on GitHub.            
        </div>  

