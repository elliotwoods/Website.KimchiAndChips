    <div class="works_02">
        <div class="text_en">
            <span class="text_en_bold">Elliot Woods</span>(UK) is a digital media artist from Manchester. He tests 
            possible futures between humans and visual design technologies (e.g. 
            cameras, projectors, computation). Towards this goal, Elliot co-founded 
            Kimchi and Chips, an experimental art studio based in Seoul with Mimi Son. 
            He applies his academic studies in physics to produce sense-able 
            phenomena from abstract domains.
            <p></p>
            Elliot is a contributor to the openFrameworks project (a ubiquitous toolkit 
            for creative coding), and an open source contributor to the VVVV platform. 
            His code is available open source and for free on GitHub.            
        </div>   
        <div class="padding-80"></div>
        <div class="text_en">
            <span class="text_en_bold">Mimi Son</span>(KR) was born in Seoul where currently she lives and works. 
            She remembers her vivid childhood fascinated by father’s painting and 
            musical talent which led her into experimenting with materials and drawing.
            <p></p>
            An obsession with geometry and Buddhist philosophy inspires her to 
            articulate space and time from various perspectives. This continuous 
            experiment has allowed her to create installation that aims to depict an 
            intersection of art and technology, material and immaterial, real and virtual, 
            presence and absence.
            <p></p>
            Over the past decade she has worked as a designer, professor, storyteller, 
            curator, and artistic director in various countries and institutions. She co-
            founded Kimchi and Chips with Elliot Woods in 2009. She completed her 
            master degree on Digital Media Art and Design at Middlesex University and 
            Interaction Design at CIID. She is currently the Adjunct Professor at Ewha 
            Womans University in Seoul and mostly working in her studio, Kimchi and 
            Chips.   
        </div>   
