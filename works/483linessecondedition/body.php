	<div class="works_02 text_en">
		The second edition of 483 lines revisits the non-imaging phenomena of analogue television, presenting two opposing scenes of 483 lines. 
		The signal artefacts become a stream of visual mass.
        <p></p>
		Two scenes of kevlar rope face one another tensioned to 5000kg. 
		The audience is presented with highly parallel lines which contrast the darker space, building panoramic moire patterns. 
		Video projection is used to address each line with digital pixels, activating visual matter within the volume of string.
        <p></p>   
        This second edition of the 483 Lines was commissioned by the ACT Festival, Gwangju 2015, continuing to celebrate and interpret the non-constructive aspects of the NTSC television format.        
