	<div class="works_02 text_en">
        Link was created for Design Korea 2010 as an interpretation of 'Convergence', the theme of the exhibition. 
        The artists present a convergence of complex, fast moving technologies from communication and visual computing with low, everyday materials. 
        <p></p>
        Furthermore, the audience is requested to cross the boundary of roles within the exhibition by themselves being presented as the exhibition. 
        By existing in the same space as the exhibition, the audience can understand that their existence within the design community can be flexible and cross-disciplinary.
        <p></p>
        As before, Kimchi and Chips look into ways that people can store their memories inside boxes, and what intuitive agency individuals can have with media technologies.
