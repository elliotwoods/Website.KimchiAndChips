	<div class="works_02 text_en">
		From World War II up until the recent end of analogue broadcasts, decades of living imagery had been constructed using the NTSC standard. 
		This standard represents a moving image frame as 483 lines of modulated light stacked from the top to the bottom of a television screen, within each line there is an analogue continuum, like the groove on a record player. 
		From Nam Jun Paik to the moon landings, pictures were being represented, archived and seen within this format, until the line made way for the pixel and the digital video revolution.
	</div>

	<div class="padding-60"></div>

	<div class="works_02">
