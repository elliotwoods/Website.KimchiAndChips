	<div class="works_02 text_en">
        Kimchi and Chips' previous work Line Segments Space fills the gallery with a web of dynamic imagery. 
        At the end of last year, the Jeju Museum of Art commissioned a new version of 
        <span class="press-link"><a href="/works/linesegmentsspace/">
		「Line Segments Space, 2013」</a></span> for the exhibition From Moment To Moment. 
		The museum designed by Korean architect Mijung Kim is a panorama of reflective floors and square concrete apertures, and these architectural characteristics demanded a reconsidering of the work and the principals by which it is arranged, leading the artists into a different path and ultimately creating a new work, 483 lines.
