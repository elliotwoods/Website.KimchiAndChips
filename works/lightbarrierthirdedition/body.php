	<div class="works_02 text_en">
		The installations present a semi-material mode of existence, materialising objects from light. 
		Light Barrier Third Edition is a new installment in this series that exploits the confusion and non-conformities at the boundary between materials and non-materials, reality and illusion, and existence and absence.	
		<p></p>
		The 6-minute sequence employs the motif of the circle to travel through themes of birth, death, and rebirth, helping shift the audience into the new mode of existence. 
		The artists use the circle often in their works to evoke the fundamentals of materials and the external connection between life and death.	
		<p></p>
		The artists are interested in how impressionist painters were inspired by the introduction of photography to create 'viewer-less images'. 
		The installation allows images to arise from the canvas, creating painting outside of perspective. 
		It is a direct approach to the artists’ theme of ‘drawing in the air’.
	</div>


