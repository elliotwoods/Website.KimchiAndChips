	<div class="othertext_center">Bring the sun down to earth</div>

	<div class="padding-50"></div>

	<div class="works_02 text_en">
		Halo was presented at the heart of Somerset House during June, 2018.
		When the artists visited Somerset House in 2016, they were struck by the quality and colour of the sunlight in the
		courtyard as it reflected from the surrounding building, and the way in which the architecture aligned with the sun
		at midday of British Summer Time. The isolated volume of natural light and wind, contained within the architecture
		of the building, presented itself as a canvas for Halo.
	</div>


