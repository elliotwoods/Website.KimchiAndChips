	<div class="works_02">
		<div class="works_name"> HALO </div>
		<div class="year_location_materials"> 2018. Somerset House, London. <br> 99 robotic mirrors, mist, sun, wind </div>
	</div>
	<div class="padding-80"></div>


	<div class="padding-1"></div>

	<div class="padding-80"></div>

	<div class="works_02 text_en">
		An array of 99 robotic mirrors track the sun’s rays to sculpt a form out of sunlight within a cloud of mist.
		Together the straight beams are shaped together to draw a circle in the air.
		Collaborating with the natural fluctuations in the climate, Halo appears only for moments when the wind, sun, water
		and technology align together, creating a form which exists between material and immaterial.
	</div>


	<div class="padding-80"></div>


	<div class="works_02 text_en">
		Kimchi and Chips explore the idea of ‘drawing in the air’, often using light as a medium.
		This is the first large-scale project in which the artists have worked solely with natural light and embraced the
		unpredictability of the weather.
		To achieve this the artists engineered a careful balance between nature and technology, between coincidence and
		inevitably.
		The moment that Halo emerges in the air the transience of life and eternal nature of the sun superimpose briefly.
	</div>

	<div class="padding-50"></div>

	<div class="othertext_center">Transient and Eternal</div>


	<div class="padding-50"></div>

	<div class="works_03 text_kr">
		헤일로는 김치앤칩스의 실천적 개념, ‘drawing in the air’의 연장선에서 탄생했다.
		<p></p>
		그간 일련의 작품들이 철저히 계획되고 제어 가능한 구조 안에서 제작되었다면, 헤일로는 우연과 필연이라는 구조 안에서 자연과 기술의 조우를 통해 심미적 조화를 발현하고자 했다.
		<p></p>
		태양과 바람, 기술과 관객의 기다림이 한 지점을 관통할 때, 헤일로는 그 모습을 삶의 찰라처럼, 자연의 영속처럼 드러내며 사라지기를 반복한다.
	</div>


